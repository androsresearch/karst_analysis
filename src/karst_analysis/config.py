"""Configuration loader for the karst_analysis pipeline.

Strategy
--------
- A canonical default config lives at ``config/pipeline_default.yml``
  and is shipped with the repo. It defines every key the pipeline can
  read. **Do not edit this file** — it is the frozen baseline.
- A user-editable config lives at ``config/pipeline.yml`` (or any
  other path given via ``--config``). The user config can be partial;
  missing keys fall back to the defaults via deep-merge.
- ``load_config(path)`` returns a fully-populated dict, validated
  against an internal schema. Any unknown key, missing required key,
  or type mismatch raises ``ConfigError`` with a clear message.

Why deep-merge
--------------
A user can override a single nested key without restating the entire
config. For example, a sensitivity-test config may contain just::

    preprocessing:
      savgol:
        window: 21

…and inherit everything else from the defaults. This makes
sensitivity sweeps cheap to write and easy to diff.

Design notes
------------
- The validator is **strict on type** but **permissive on missing keys**:
  if the user's YAML omits a key, we use the default. If a key has the
  wrong type (e.g. ``window: "eleven"``), we fail loudly.
- Unknown keys are warnings, not errors. This is intentional: the
  pipeline may add new keys, and we don't want old configs to break.
  The warning surfaces typos without aborting batches.
- The ``params_for_run_ledger`` helper extracts the subset relevant
  to a single run (e.g. just savgol params for a savgol run) so the
  ``runs.csv`` ledger entry is concise but traceable.
"""

from __future__ import annotations

import warnings
from copy import deepcopy
from pathlib import Path
from typing import Any, Optional

import yaml


# Path to the frozen baseline config — resolved relative to the project root.
_DEFAULT_CONFIG_NAME = "pipeline_default.yml"


class ConfigError(ValueError):
    """Raised on config loading / validation errors."""


# ──────────────────────────────────────────────────────────────────────
#  Schema (declarative)
#
#  Each leaf is a tuple (expected_type, allow_none). Compound nodes are
#  nested dicts. This is intentionally simple — we don't pull in
#  pydantic / cerberus / jsonschema. The schema lives next to the
#  defaults so adding a key requires editing two places (default value
#  and schema), which is easy to keep in sync.
# ──────────────────────────────────────────────────────────────────────

# Top-level config blocks that exist for visualization modules but
# whose internal keys are not validated centrally — those modules
